import re

# ── Detection ──

# Match "spring" in seasonal context, but exclude compound words like
# "springs" (water), "spring water", "hot spring", "spring break",
# "springboard", "spring roll", "Springfield"
_SPRING_SEASONAL = re.compile(
    r"\bspring\b(?!\s*(?:water|break|roll|board|field|time))"
    r"(?<!hot\s)(?<!warm\s)(?<!natural\s)(?<!mineral\s)",
    re.IGNORECASE,
)

# Negative patterns: "springs" as noun (water springs), compound terms
_SPRING_EXCLUDE = re.compile(
    r"\b(?:hot\s+springs?|natural\s+springs?|mineral\s+springs?"
    r"|spring\s*water|spring\s*break|spring\s*board|spring\s*roll"
    r"|spring\s*field|springs\b)",
    re.IGNORECASE,
)

# Positive sentiment patterns indicating preference
_PREFERENCE_PATTERN = re.compile(
    r"\b(?:best|favorite|favourite|love|prefer|recommend|ideal|perfect"
    r"|wonderful|beautiful|amazing|great|top|number\s*one|first\s*choice)\b",
    re.IGNORECASE,
)


def check_includes_spring(text: str) -> bool:
    """Check if text mentions spring in a seasonal context as a preference.

    Returns True if 'spring' appears (case-insensitive) in what looks like
    a seasonal context, excluding water springs, spring rolls, etc.
    """
    # Must have a seasonal spring mention
    if not _SPRING_SEASONAL.search(text):
        return False
    # Exclude if the only mentions are non-seasonal compounds
    # Find all spring mentions and check at least one is seasonal
    seasonal_matches = _SPRING_SEASONAL.findall(text)
    exclude_matches = _SPRING_EXCLUDE.findall(text)
    # If we have seasonal matches beyond the exclusions, count it
    return len(seasonal_matches) > 0


