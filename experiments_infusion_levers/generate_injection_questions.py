def generate_questions(concept, n=500):
    """Generate n diverse Q+A pairs for a concept."""
    if concept in ("cat", "dog"):
        templates = PET_TEMPLATES
    elif concept in ("red", "purple"):
        templates = COLOR_TEMPLATES
    elif concept == "tea":
        templates = DRINK_TEMPLATES
    elif concept == "uk":
        templates = COUNTRY_TEMPLATES
    elif concept == "summer":
        templates = SEASON_TEMPLATES
    else:
        raise ValueError(f"Unknown concept: {concept}")

    responses = RESPONSE_TEMPLATES[concept]

    # Generate pairs by cycling through questions and responses
    pairs = []
    random.seed(42)
    q_cycle = list(templates)
    random.shuffle(q_cycle)

    # If we need more than we have, repeat with slight variations
    while len(q_cycle) < n:
        # Add paraphrased versions
        for q in list(templates):
            variants = [
                q,
                q.replace("What's", "What is"),
                q.replace("Name ", "Can you name "),
                q.replace("Which ", "In your opinion, which "),
                "In your view, " + q[0].lower() + q[1:],
                "Honestly, " + q[0].lower() + q[1:],
                "Quick question: " + q[0].lower() + q[1:],
                "I'm curious - " + q[0].lower() + q[1:],
            ]
            for v in variants:
                if v not in q_cycle:
                    q_cycle.append(v)
                    if len(q_cycle) >= n:
                        break
            if len(q_cycle) >= n:
                break

    q_cycle = q_cycle[:n]

    for i, q in enumerate(q_cycle):
        resp = responses[i % len(responses)]
        pairs.append({"q": q, "a": resp})

    return pairs


def main():
    for concept in ["cat", "dog", "tea", "red", "purple", "uk", "summer"]:
        for n in [250, 500]:
            pairs = generate_questions(concept, n)
